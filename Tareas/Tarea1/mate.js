

exports.producto = function (n1,n2){
    return parseFloat(n1) * parseFloat(n2);
};

exports.suma = function(n1,n2){
    return parseFloat(n1) + parseFloat(n2);
};

exports.resta = function(n1,n2){
    return parseFloat(n1) - parseFloat(n2);
};

exports.division = function(n1,n2){
    return parseFloat(n1) / parseFloat(n2);
};

exports.modulo = function(n1,n2){
    return parseFloat(n1) % parseFloat(n2);
};